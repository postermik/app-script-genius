import type { NarrativeOutputData } from "@/types/narrative";
import type { DeckTheme } from "@/components/SlidePreview";
import {
  CHAR_LIMITS, getHeadlineFontSize, truncate, resolveLayout,
} from "@/lib/slideLayouts";

interface ExportOptions {
  output: NarrativeOutputData;
  isPro: boolean;
  excludedSlides: Set<number>;
  slideOrder: number[];
  deckTheme: DeckTheme;
}

type RGB = [number, number, number];

interface PdfColors {
  bg: RGB; fg: RGB; primary: RGB; muted: RGB; accent: RGB;
}

function getThemeColors(theme: DeckTheme): PdfColors {
  const hex = (h: string): RGB => {
    const c = h.replace("#", "");
    return [parseInt(c.substring(0, 2), 16), parseInt(c.substring(2, 4), 16), parseInt(c.substring(4, 6), 16)];
  };
  if (theme.scheme === "light") {
    return { bg: [255, 255, 255], fg: [26, 26, 46], primary: [59, 130, 246], muted: [107, 114, 128], accent: [59, 130, 246] };
  }
  if (theme.scheme === "custom") {
    const bgC = hex(theme.secondary || "#0b0f14");
    const brightness = (bgC[0] * 299 + bgC[1] * 587 + bgC[2] * 114) / 1000;
    return {
      bg: bgC,
      fg: brightness > 128 ? [26, 26, 46] : [220, 224, 232],
      primary: hex(theme.primary || "#3b82f6"),
      muted: [107, 114, 128],
      accent: hex(theme.accent || "#1e3a5f"),
    };
  }
  return { bg: [11, 15, 20], fg: [220, 224, 232], primary: [59, 130, 246], muted: [107, 114, 128], accent: [59, 130, 246] };
}

// Map PPTX font sizes to PDF (jsPDF uses slightly different scaling)
function pdfHeadlineFontSize(text: string): number {
  const pptxSize = getHeadlineFontSize(text);
  // Scale down slightly for PDF since jsPDF renders larger
  if (pptxSize >= 28) return 24;
  if (pptxSize >= 24) return 20;
  if (pptxSize >= 22) return 18;
  return 16;
}

function slideFields(raw: any) {
  const headline = truncate(typeof raw === "string" ? raw : (raw.headline || ""), CHAR_LIMITS.HEADLINE_MAX);
  const subheadline = truncate(typeof raw === "string" ? "" : (raw.subheadline || raw.subheader || ""), CHAR_LIMITS.SUBHEADLINE_MAX);
  const bodyContent: string[] = (typeof raw === "string" ? [] : (Array.isArray(raw.bodyContent) ? raw.bodyContent : []))
    .map((b: string) => truncate(b.replace(/^[-\u2022*]\s*/, ""), CHAR_LIMITS.BULLET_MAX));
  const categoryLabel = truncate(typeof raw === "string" ? "" : (raw.categoryLabel || ""), CHAR_LIMITS.CATEGORY_MAX);
  const closingStatement = truncate(typeof raw === "string" ? "" : (raw.closingStatement || ""), CHAR_LIMITS.CLOSING_MAX);
  const layoutRecommendation = typeof raw === "string" ? "" : (raw.layoutRecommendation || "");
  const selectedLayout = typeof raw === "string" ? undefined : (raw.selectedLayout || undefined);
  return { headline, subheadline, bodyContent, categoryLabel, closingStatement, layoutRecommendation, selectedLayout };
}

export async function exportPdf({ output, isPro, excludedSlides, slideOrder, deckTheme }: ExportOptions) {
  const { jsPDF } = await import("jspdf");
  const title = (output as any).title || "Narrative Deck";
  const colors = getThemeColors(deckTheme);

  const pdf = new jsPDF({ orientation: "landscape", unit: "in", format: [10, 7.5] });
  const W = 10;
  const H = 7.5;
  const ML = 0.75;
  const CW = W - ML * 2; // 8.5in content width

  const setC = (c: RGB, type: "fill" | "text" = "text") => {
    if (type === "fill") pdf.setFillColor(c[0], c[1], c[2]);
    else pdf.setTextColor(c[0], c[1], c[2]);
  };

  const invertOnAccent = (): RGB => {
    const b = (colors.bg[0] * 299 + colors.bg[1] * 587 + colors.bg[2] * 114) / 1000;
    return b > 128 ? [255, 255, 255] : colors.bg;
  };

  const drawBg = () => {
    setC(colors.bg, "fill");
    pdf.rect(0, 0, W, H, "F");
    setC(colors.accent, "fill");
    pdf.rect(0, 0, W, 0.04, "F");
  };

  // ── Title slide ──
  drawBg();
  setC(colors.accent, "fill");
  pdf.rect(0, H - 0.06, W, 0.06, "F");
  pdf.setFont("helvetica", "bold");
  pdf.setFontSize(28);
  setC(colors.primary);
  pdf.text(title, W / 2, 3.0, { align: "center" });
  pdf.setFontSize(11);
  setC(colors.muted);
  pdf.text(output.mode.replace(/_/g, " ").toUpperCase(), W / 2, 3.7, { align: "center" });
  if (!isPro) { pdf.setFontSize(9); setC(colors.muted); pdf.text("Generated by Rhetoric", W / 2, 6.0, { align: "center" }); }

  // ── Content slides ──
  const d = (output.data || (output as any).supporting || {}) as any;
  const del = (output as any).deliverable || {};
  const framework = d.deckFramework || del.deckFramework || d.boardDeckOutline || del.boardDeckOutline || [];
  const ordered = slideOrder.length > 0 ? slideOrder : framework.map((_: any, i: number) => i);
  const active = ordered.filter((i: number) => !excludedSlides.has(i));

  for (const idx of active) {
    const raw = framework[idx];
    if (!raw) continue;

    pdf.addPage();
    drawBg();

    const f = slideFields(raw);
    const layout = resolveLayout(f.layoutRecommendation, f.selectedLayout);
    let y = 0.6;

    // ── Category label (shared across layouts) ──
    if (f.categoryLabel) {
      pdf.setFont("helvetica", "bold");
      pdf.setFontSize(9);
      setC(colors.accent);
      pdf.text(f.categoryLabel.toUpperCase(), ML, y);
      y += 0.3;
    }

    // ── Headline (auto-sized, shared) ──
    const hSize = pdfHeadlineFontSize(f.headline);
    pdf.setFont("helvetica", "bold");
    pdf.setFontSize(hSize);
    setC(colors.primary);
    const headlineLines = pdf.splitTextToSize(f.headline, CW);
    pdf.text(headlineLines, ML, y);
    const lineH = hSize / 72 * 1.3;
    y += headlineLines.length * lineH + 0.15;

    // ── Layout-specific rendering ──
    if (layout === "statement") {
      // Statement: just headline centered + closing at bottom
      if (f.subheadline) {
        pdf.setFont("helvetica", "normal");
        pdf.setFontSize(14);
        setC(colors.muted);
        const subLines = pdf.splitTextToSize(f.subheadline, CW * 0.75);
        pdf.text(subLines, ML, y + 0.3);
      }
      if (f.closingStatement) {
        setC(colors.accent, "fill");
        pdf.rect(0, H - 1.0, W, 1.0, "F");
        pdf.setFont("helvetica", "bold");
        pdf.setFontSize(13);
        setC(invertOnAccent());
        pdf.text(f.closingStatement, ML, H - 0.5);
      }

    } else if (layout === "cards") {
      // Data callout / cards: render colored card backgrounds
      const items = f.bodyContent.slice(0, 3);
      const dp = typeof raw !== "string" ? (raw?.metadata?.dataPoints || []) : [];
      const cardCount = Math.max(dp.length, Math.min(items.length, 3));
      if (cardCount > 0) {
        const gap = 0.3;
        const cardW = (CW - gap * (cardCount - 1)) / cardCount;
        const cardH = 1.8;
        for (let i = 0; i < cardCount; i++) {
          const cx = ML + i * (cardW + gap);
          setC(colors.accent, "fill");
          pdf.roundedRect(cx, y, cardW, cardH, 0.06, 0.06, "F");
          if (dp[i]) {
            pdf.setFont("helvetica", "bold");
            pdf.setFontSize(30);
            setC(invertOnAccent());
            pdf.text(dp[i], cx + 0.2, y + 0.55);
          }
          if (items[i]) {
            pdf.setFont("helvetica", "normal");
            pdf.setFontSize(10);
            setC(invertOnAccent());
            const cardLines = pdf.splitTextToSize(truncate(items[i], 80), cardW - 0.4);
            pdf.text(cardLines, cx + 0.2, y + (dp[i] ? 0.95 : 0.35));
          }
        }
        y += cardH + 0.25;
      }
      // Remaining bullets
      const rest = f.bodyContent.slice(cardCount);
      if (rest.length > 0) {
        pdf.setFont("helvetica", "normal");
        pdf.setFontSize(11);
        for (const line of rest) {
          if (y > H - 0.8) break;
          setC(colors.accent);
          pdf.text("\u2022", ML, y);
          setC(colors.fg);
          const wrapped = pdf.splitTextToSize(line, CW - 0.3);
          pdf.text(wrapped, ML + 0.25, y);
          y += wrapped.length * 0.2 + 0.1;
        }
      }

    } else if (layout === "matrix") {
      // Matrix: 2x2 grid
      const items = f.bodyContent.slice(0, 4);
      if (items.length >= 2) {
        const cols = 2;
        const rows = Math.ceil(items.length / cols);
        const gap = 0.2;
        const cellW = (CW - gap) / cols;
        const cellH = (H - y - 0.6) / rows - gap / 2;
        for (let i = 0; i < items.length; i++) {
          const col = i % cols;
          const row = Math.floor(i / cols);
          const cx = ML + col * (cellW + gap);
          const cy = y + row * (cellH + gap);
          setC(colors.accent, "fill");
          pdf.roundedRect(cx, cy, cellW, cellH, 0.06, 0.06, "F");
          // Left accent bar
          setC(colors.primary, "fill");
          pdf.rect(cx, cy, 0.04, cellH, "F");
          pdf.setFont("helvetica", "normal");
          pdf.setFontSize(11);
          setC(invertOnAccent());
          const cellLines = pdf.splitTextToSize(truncate(items[i], 120), cellW - 0.5);
          pdf.text(cellLines, cx + 0.2, cy + 0.3);
        }
      }

    } else if (layout === "timeline") {
      // Timeline: horizontal nodes
      const items = f.bodyContent.slice(0, 5);
      if (items.length >= 2) {
        const n = items.length;
        const spacing = CW / n;
        const r = 0.15;
        const nodeY = y + 0.1;
        // Connecting line
        setC(colors.accent, "fill");
        pdf.rect(ML + spacing / 2, nodeY + r - 0.01, CW - spacing, 0.025, "F");
        for (let i = 0; i < n; i++) {
          const cx = ML + spacing * i + spacing / 2;
          // Circle
          setC(colors.primary, "fill");
          pdf.circle(cx, nodeY + r, r, "F");
          // Number
          pdf.setFont("helvetica", "bold");
          pdf.setFontSize(11);
          setC(invertOnAccent());
          pdf.text(String(i + 1), cx, nodeY + r + 0.05, { align: "center" });
          // Label below
          pdf.setFont("helvetica", "normal");
          pdf.setFontSize(10);
          setC(colors.fg);
          const label = pdf.splitTextToSize(truncate(items[i], 80), spacing - 0.3);
          pdf.text(label, cx, nodeY + r * 2 + 0.25, { align: "center" });
        }
      }

    } else if (layout === "two-column") {
      // Two column: text left, accent area right
      const leftW = CW * 0.55;
      const rightW = CW * 0.38;
      const rightX = ML + CW * 0.6;

      if (f.subheadline) {
        pdf.setFont("helvetica", "normal");
        pdf.setFontSize(12);
        setC(colors.muted);
        const subLines = pdf.splitTextToSize(f.subheadline, leftW);
        pdf.text(subLines, ML, y);
        y += subLines.length * 0.2 + 0.15;
      }
      // Bullets on left
      pdf.setFont("helvetica", "normal");
      pdf.setFontSize(11);
      for (const line of f.bodyContent.slice(0, 5)) {
        if (y > H - 0.8) break;
        setC(colors.accent);
        pdf.text("\u2022", ML, y);
        setC(colors.fg);
        const wrapped = pdf.splitTextToSize(line, leftW - 0.3);
        pdf.text(wrapped, ML + 0.25, y);
        y += wrapped.length * 0.2 + 0.1;
      }
      // Right accent area
      setC(colors.accent, "fill");
      pdf.roundedRect(rightX, 1.0, rightW, H - 1.6, 0.08, 0.08, "F");

    } else {
      // Default bullets layout
      if (f.subheadline) {
        pdf.setFont("helvetica", "normal");
        pdf.setFontSize(13);
        setC(colors.muted);
        const subLines = pdf.splitTextToSize(f.subheadline, CW);
        pdf.text(subLines, ML, y);
        y += subLines.length * 0.22 + 0.15;
      }

      if (f.bodyContent.length > 0) {
        pdf.setFont("helvetica", "normal");
        const bSize = f.bodyContent.length > 4 ? 11 : 12;
        pdf.setFontSize(bSize);
        for (const line of f.bodyContent.slice(0, 6)) {
          if (y > H - 1) break;
          setC(colors.accent);
          pdf.text("\u2022", ML, y);
          setC(colors.fg);
          const wrapped = pdf.splitTextToSize(line, CW - 0.3);
          pdf.text(wrapped, ML + 0.25, y);
          y += wrapped.length * 0.2 + 0.1;
        }
      }

      if (f.closingStatement && y < H - 0.5) {
        pdf.setFont("helvetica", "bold");
        pdf.setFontSize(10);
        setC(colors.accent);
        pdf.text(f.closingStatement, ML, H - 0.6);
      }
    }

    // Watermark
    if (!isPro) {
      pdf.setFontSize(7);
      setC(colors.muted);
      pdf.text("Generated by Rhetoric", ML, H - 0.3);
    }
  }

  pdf.save(`${title.replace(/[^a-zA-Z0-9 ]/g, "")}.pdf`);
}