import type { NarrativeOutputData } from "@/types/narrative";
import type { DeckTheme } from "@/components/SlidePreview";

interface ExportOptions {
  output: NarrativeOutputData;
  isPro: boolean;
  excludedSlides: Set<number>;
  slideOrder: number[];
  deckTheme: DeckTheme;
}

function getThemeColors(theme: DeckTheme) {
  if (theme.scheme === "light") {
    return { bg: [255, 255, 255], fg: [26, 26, 46], muted: [107, 114, 128], accent: [59, 130, 246] };
  }
  if (theme.scheme === "custom") {
    const hex = (h: string) => {
      const c = h.replace("#", "");
      return [parseInt(c.substring(0, 2), 16), parseInt(c.substring(2, 4), 16), parseInt(c.substring(4, 6), 16)] as [number, number, number];
    };
    const bgC = hex(theme.secondary || "#0b0f14");
    const brightness = (bgC[0] * 299 + bgC[1] * 587 + bgC[2] * 114) / 1000;
    return {
      bg: bgC,
      fg: brightness > 128 ? [26, 26, 46] : [220, 224, 232],
      muted: [107, 114, 128],
      accent: hex(theme.primary || "#3b82f6"),
    };
  }
  return { bg: [11, 15, 20], fg: [220, 224, 232], muted: [107, 114, 128], accent: [59, 130, 246] };
}

export async function exportPdf({ output, isPro, excludedSlides, slideOrder, deckTheme }: ExportOptions) {
  const { jsPDF } = await import("jspdf");
  const title = (output as any).title || "Narrative Deck";
  const colors = getThemeColors(deckTheme);

  // Landscape orientation, inches unit for consistency
  const pdf = new jsPDF({ orientation: "landscape", unit: "in", format: [10, 7.5] });

  const W = 10;
  const H = 7.5;
  const MARGIN = 0.75;
  const CONTENT_W = W - MARGIN * 2;

  const setColor = (c: number[], type: "fill" | "text" = "text") => {
    if (type === "fill") pdf.setFillColor(c[0], c[1], c[2]);
    else pdf.setTextColor(c[0], c[1], c[2]);
  };

  const drawBg = () => {
    setColor(colors.bg, "fill");
    pdf.rect(0, 0, W, H, "F");
    // Accent bar at top
    setColor(colors.accent, "fill");
    pdf.rect(0, 0, W, 0.04, "F");
  };

  // Title slide
  drawBg();
  pdf.setFont("helvetica", "bold");
  pdf.setFontSize(28);
  setColor(colors.fg);
  pdf.text(title, W / 2, 2.5, { align: "center" });

  pdf.setFontSize(12);
  setColor(colors.accent);
  pdf.text(output.mode.replace(/_/g, " ").toUpperCase(), W / 2, 3.2, { align: "center" });

  if (!isPro) {
    pdf.setFontSize(9);
    setColor(colors.muted);
    pdf.text("Generated by Rhetoric", W / 2, 5.5, { align: "center" });
  }

  // Content slides
  const d = (output.data || (output as any).supporting || {}) as any;
  const del = (output as any).deliverable || {};
  const framework = d.deckFramework || del.deckFramework || d.boardDeckOutline || del.boardDeckOutline || [];
  const orderedIndices = slideOrder.length > 0 ? slideOrder : framework.map((_: any, i: number) => i);
  const activeIndices = orderedIndices.filter((i: number) => !excludedSlides.has(i));

  for (const originalIdx of activeIndices) {
    const raw = framework[originalIdx];
    if (!raw) continue;

    pdf.addPage();
    drawBg();

    const headline = typeof raw === "string" ? raw : (raw.headline || "");
    const subheadline = typeof raw === "string" ? "" : (raw.subheadline || raw.subheader || "");
    const bodyContent: string[] = typeof raw === "string" ? [] : (Array.isArray(raw.bodyContent) ? raw.bodyContent : []);
    const categoryLabel = typeof raw === "string" ? "" : (raw.categoryLabel || "");
    const closingStatement = typeof raw === "string" ? "" : (raw.closingStatement || "");

    let y = 0.5;

    // Category label
    if (categoryLabel) {
      pdf.setFont("helvetica", "bold");
      pdf.setFontSize(9);
      setColor(colors.accent);
      pdf.text(categoryLabel.toUpperCase(), MARGIN, y);
      y += 0.3;
    }

    // Headline
    pdf.setFont("helvetica", "bold");
    pdf.setFontSize(headline.length > 50 ? 20 : 24);
    setColor(colors.fg);
    const headlineLines = pdf.splitTextToSize(headline, CONTENT_W);
    pdf.text(headlineLines, MARGIN, y);
    y += headlineLines.length * 0.35 + 0.15;

    // Accent line
    setColor(colors.accent, "fill");
    pdf.rect(MARGIN, y, 1.5, 0.025, "F");
    y += 0.25;

    // Subheadline
    if (subheadline) {
      pdf.setFont("helvetica", "normal");
      pdf.setFontSize(14);
      setColor(colors.muted);
      const subLines = pdf.splitTextToSize(subheadline, CONTENT_W);
      pdf.text(subLines, MARGIN, y);
      y += subLines.length * 0.25 + 0.2;
    }

    // Body content
    if (bodyContent.length > 0) {
      pdf.setFont("helvetica", "normal");
      pdf.setFontSize(12);
      setColor(colors.fg);
      for (const line of bodyContent.slice(0, 12)) {
        const clean = line.replace(/^[-•*]\s*/, "");
        const wrapped = pdf.splitTextToSize(clean, CONTENT_W - 0.3);

        // Bullet dot
        setColor(colors.accent);
        pdf.setFontSize(12);
        pdf.text("\u2022", MARGIN, y);

        setColor(colors.fg);
        pdf.text(wrapped, MARGIN + 0.25, y);
        y += wrapped.length * 0.22 + 0.1;

        if (y > H - 1) break;
      }
    }

    // Closing statement
    if (closingStatement && y < H - 0.5) {
      pdf.setFont("helvetica", "bold");
      pdf.setFontSize(10);
      setColor(colors.accent);
      pdf.text(closingStatement, MARGIN, H - 0.6);
    }

    // Watermark
    if (!isPro) {
      pdf.setFontSize(7);
      setColor(colors.muted);
      pdf.text("Generated by Rhetoric", MARGIN, H - 0.3);
    }
  }

  pdf.save(`${title.replace(/[^a-zA-Z0-9 ]/g, "")}.pdf`);
}
